# coding: UTF-8

t = True
f = False

print("init value:")
print("t: {} | f: {}".format(t, f))

print("Logic Symbol:")
print("t and f : {}".format(t and f)) # 布尔"与" - 如果 x 为 False，x and y 返回 False，否则它返回 y 的计算值。

print("t or f : {}".format(t or f)) #布尔"或" - 如果 x 是非 0，它返回 x 的值，否则它返回 y 的计算值。

print("not t : {}".format(not t)) #布尔"非" - 如果 x 为 True，返回 False 。如果 x 为 False，它返回 True。
