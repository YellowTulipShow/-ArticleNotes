# coding: UTF-8

a = 6
b = 2
print("init value:")
print("a : {} | b : {}".format(a, b))

print("Arithmetic Symbol:")
print("a + b : {}".format(a + b)) #加 - 两个对象相加
print("a - b : {}".format(a - b)) #减 - 得到负数或是一个数减去另一个数
print("a * b : {}".format(a * b)) #乘 - 两个数相乘或是返回一个被重复若干次的字符串
print("a / b : {}".format(a / b)) #除 - x除以y
print("a % b : {}".format(a % b)) #取模 - 返回除法的余数
print("a ** b : {}".format(a ** b)) #幂 - 返回x的y次幂
print("a // b : {}".format(a // b)) #取整除 - 返回商的整数部分
